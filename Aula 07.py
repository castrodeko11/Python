# Ordem  de Precedência

# 1 ()
# 2 **
# 3 * / // %
# 4 + -

nome = input("Qual sua nome: ")
print("Prazer {:20}!".format(nome)) # Usando 20 Caracteres
print("Prazer {:>20}!".format(nome)) # Alinhar na Direita
print("Prazer {:<20}!".format(nome)) # Alinhar na esquerda
print("Prazer {:^20}!".format(nome)) # Centralizar
print("Prazer {:=^20}!".format(nome)) # Centralizar e preencher ao redor


